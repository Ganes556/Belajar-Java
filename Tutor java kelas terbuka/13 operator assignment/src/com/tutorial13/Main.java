public class Main {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // operator assignment
        
        // assignment operator jumlah
        int a = 5;
        a +=5;
        System.out.println("hasil assgment operator jumlah = " + a);
        // assignment operator pengurangan 
        int b = 5;
        b -=5;
        System.out.println("hasil assgment operator pengurangan = " + b);
         // assignment operator perkalian 
        int c = 5;
        c *=5;
        System.out.println("hasil assgment operator perkalian = " + c);
         // assignment operator pembagian 
        int d = 5;
        d /=5;
        System.out.println("hasil assgment operator pembagian = " + d);
         // assignment operator modulus
        int e = 5;
        e %=5;
        System.out.println("hasil assgment operator modulus = " + e);
    }
    
}
